        {
            if (composite == null)
            {
                throw new ArgumentNullException("composite");
            }
            if (composite.BoolValue)
            {
                composite.StringValue += "Suffix";
            }
            return composite;
        }

        public string GetMessage()
        {
           return "Hola Mundo";
        }

        public int insertClient(string email, string password, string name, string birthday)
        {
